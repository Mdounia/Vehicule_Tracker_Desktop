    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        Vehicule vehicule = (Vehicule) vehicules.getSelectedItem();
        GPSTracker tracker = (GPSTracker) trackers.getSelectedItem();
        java.util.Date dateDebut = JDateD.getDate();
            if (vehiculeGPSTrackerDao.create(new VehiculeGPSTracker(dateDebut, null, vehicule, tracker))) {
                JOptionPane.showMessageDialog(this, "Bien ajoutée");
                JDateD.setCalendar(null);
                load();
            } else {
                JOptionPane.showMessageDialog(this, "erreur d\'ajout");
            }
        
        
    }//GEN-LAST:event_addActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
         int rep = JOptionPane.showConfirmDialog(this, "êtes-vous sûr de vouloir supprimer cette affectation");
        if (rep == 0) {
            vehiculeGPSTrackerDao.delete(id);
            JOptionPane.showMessageDialog(this, "Bien supprimé");     
            JDateD.setCalendar(null);
            JDateF.setCalendar(null);
            load();
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void JTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableMouseClicked
        id =  (VehiculeGPSTrackerKey)model.getValueAt(JTable.getSelectedRow(), 0);
        vehicules.setSelectedItem(model.getValueAt(JTable.getSelectedRow(), 1).toString());
        trackers.setSelectedItem(model.getValueAt(JTable.getSelectedRow(), 2).toString());
        JDateD.setDate((java.util.Date) model.getValueAt(JTable.getSelectedRow(), 3));
        JDateF.setDate((java.util.Date) model.getValueAt(JTable.getSelectedRow(), 4));
        JDateD.setEnabled(false);
        vehicules.setEnabled(false);
        trackers.setEnabled(false);

    }//GEN-LAST:event_JTableMouseClicked

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        int rep=JOptionPane.showConfirmDialog(this, "êtes-vous sûr de vouloir modifier cette affectation");
        if (rep == 0) {
            Vehicule vehicule = (Vehicule) vehicules.getSelectedItem();
            GPSTracker tracker = (GPSTracker) trackers.getSelectedItem();
            java.util.Date dateDebut = JDateD.getDate();
            java.util.Date dateFin = JDateF.getDate();
            vehiculeGPSTrackerDao.update(new VehiculeGPSTracker(id, dateDebut,dateFin,vehicule,tracker));
            JOptionPane.showMessageDialog(this, "Desaffectation");
            JDateD.setCalendar(null);
            JDateF.setCalendar(null);
            load();

        }
    }//GEN-LAST:event_updateActionPerformed


