        void loadVehicules(){
           List<Vehicule> removedVehicules=new ArrayList();
           List<Vehicule> vehiculesR=vehiculeDao.findAll();
            for(Vehicule v:vehiculesR){
                for(VehiculeGPSTracker vg:vehiculeGPSTrackerDao.findAll()){
                    if(v.getMatricule()==vg.getVehicule().getMatricule()){
                        removedVehicules.add(v);
                    }
                }
            }
            vehiculesR.removeAll(removedVehicules);
            for(Vehicule v1:vehiculesR){
                vehicules.addItem(v1);
            }
        }
        void loadGPSTrackers(){
            for(GPSTracker g:gpstrackerDao.findAll()){
                trackers.addItem(g);
            }
        }
         void load() {
        model.setRowCount(0);
        for (VehiculeGPSTracker v : vehiculeGPSTrackerDao.findAll()) {
            model.addRow(new Object[]{
                v.getId(),
                v.getVehicule().getMatricule(),
                v.getGpstracker().getSimNumber(),
                v.getDateDebut(),
                v.getDateFin()
            });
        }
    }

